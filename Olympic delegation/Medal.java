public class Medal {
    
    private Integer medalID;
    
    private Player player;
    
    public Integer getMedalID() {
        return this.medalID;
    }
    
    public Integer setMedalID(Integer medalID) {
        this.medalID = medalID;
    }
    
    public Player getPlayer() {
        return this.player;
    }
    
    public Player setPlayer(Player player) {
        this.player = player;
    }
    
}
