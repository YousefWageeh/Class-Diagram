public class Person {
    
    private String name;
    
    private String address;
    
    public String getName() {
        return this.name;
    }
    
    public String setName(String name) {
        this.name = name;
    }
    
    public String getAddress() {
        return this.address;
    }
    
    public String setAddress(String address) {
        this.address = address;
    }
    
    
    
}
